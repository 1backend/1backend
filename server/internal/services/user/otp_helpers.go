package userservice

import (
	"crypto/rand"
	"math/big"
)

func generateOtpCode(length int) (string, error) {
	if length <= 0 {
		length = 6
	}

	code := make([]byte, length)
	for i := range code {
		n, err := rand.Int(rand.Reader, big.NewInt(10))
		if err != nil {
			return "", err
		}
		code[i] = byte('0') + byte(n.Int64())
	}

	return string(code), nil
}
